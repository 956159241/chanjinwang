                <div class="ui segment section">
                  <p>曾经在2005—2007年行业爆发期囤积下的大量普洱茶近期逐渐在市场中露脸。有业内人士透露，由于普洱茶金融交易市场规模不断扩大，机构投资者、基金开始斥资收购该时期的囤货，资金规模最大可达3~4亿元。资金从新茶转入年份茶会否为市场带来鉴定难题？经历过前两波资金炒作潮的普洱茶市场面临的是新的机遇还是新的冲击？</p>
                  <p>“普洱茶市场高度发展的时期必有资金进入。”在昨日广州茶业协会普洱茶年份鉴定委员会成立现场，大圆普洱交易中心副总裁张赢鸿接受记者采访时表示，不管是2006年还是2013年的普洱茶市场行情，都是因为大量外部资金进入市场而形成的：“当时的方式属于投资性炒作。”而最近普洱茶市场又出现资金运作的信号。</p>
                  <h4>10年年份茶成投资热点</h4>
                  <p>广州茶业协会副会长钟广林透露，2005~2007年之间，茶企生产了大量产品，在行情走下坡路后囤积在手中，近期有机构投资者在市场上搜罗这个时期的好茶，准备上市。</p>
                  <p>他告诉记者，搜罗这个时期的产品有几个原因，一个是目前处于市场调整期，普洱茶金融市场走向成熟，投资者认为这是合适入市的时间点；二是对于普洱茶来说，存放10年正是开始转化、成熟的时候，最适合饮用、性价比最高，所以投资这个时期的产品正当时；三是这个阶段的产品有一定的数量，而且一直潜藏不出，适合作为投资对象。</p>
                  <p>记者从业内了解到，目前正在斥资进入市场的投资者包括机构投资人、投资基金，规模从5000万元至三四亿元不等。张赢鸿认为，这一轮行情与前两波应该有所不同：“之前都是投资资金炒作，炒作范围很小，主要集中在珠三角茶叶市场中，商户的投入少则数百万元，多则数千万元，而如今投资者的构成中，很大部分来自散户。”他认为，普洱茶金融平台的意义在于令茶客和投资者集中在一个平台上，平台提供标准的茶叶仓储条件，在一定的封闭期内保管好普洱茶，令其随着时间转化、升值。</p>
                  <h4>年份茶炒作带来鉴定难题</h4>
                  <p>钟广林告诉记者，之前的炒作行情令普洱茶市场留下了不少问题：“2003~2010年投资者囤积的那些茶叶中，由于不同的环境、不同的储藏条件，有些茶叶品质出现问题。”他认为，这跟当时的资金投入没有持续性、业内没有统一仓储标准和机构有关。</p>
                  <p>“当资金再度入市，并且相中的不只是新茶而是年份茶，就意味着未来年份茶将作为新的市场炒作热点。”一个普洱茶投资者告诉记者，他自己也有计划今年出手手中的年份茶，置换成新茶。不过，他担忧的是，当大量年份茶出现在市场中，鉴定就将成为一个难题。</p>
                  <p>钟广林认为，这就是广州茶业协会普洱茶年份鉴定委员会成立的原因：“市场需要有第三方权威鉴定机构，行业也需要有规范的标准。”</p>
                  <h4>业内观点：市场或难回原来的高位</h4>
                  <p>对于普洱茶可能迎来的新一波行情，市场人士并没有过于急切：“整个行业的玩法不一样了，两极分化越来越明显，整体市场已不可能走到原来的高位。”</p>
                  <p>钟广林认为，第一波行情是没有品质和品牌的“混战”，第二波行情是品质的竞争，而第三波行情将是品牌和品质的竞争：“现在可以说是从品质加品牌的成长过程。”他预测今年下半年整个普洱茶市场温度会比去年有一定的提升。</p>
